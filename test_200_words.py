def run_tests():
    total_words = len(test_cases)
    print(f"Running {total_words} tests...")
    print("=" * 80)
    for i, word in enumerate(test_cases, 1):
        buckw_raw = arabic_to_buckwalter(word)
        mantoq_phonemes = process_utterance(buckw_raw)
        ipa_stressed = arabic_to_ipa(word, space_separated=True)
        
        print(f"[{i:03d}/{total_words}] Original : {word}")
        print(f"          Mantoq Phs: {mantoq_phonemes}")
        print(f"          IPA Stress: {ipa_stressed}")
        print("-" * 80)

if __name__ == "__main__":
    run_tests()
